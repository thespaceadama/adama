number_1 = int(input('Ввведите первое число: '))
number_2 = int(input('Ввведите второе число: '))
number_3 = int(input('Ввведите третье число: '))
number_4 = int(input('Ввведите четвертое число: '))

result = number_1 + number_2 + number_3 + number_4
print(number_1, '+', number_2, '+', number_3, '+', number_4, '=', result)

result_2 = number_1 - number_2 - number_3 - number_4
print(number_1, '-', number_2, '-', number_3, '-', number_4, '=', result_2)

result_3 = number_1 * number_2 * number_3 * number_4
print(number_1, '*', number_2, '*', number_3, '*', number_4, '=', result_3)

result_6 = number_1 / number_2 / number_3 / number_4
print(number_1, '/', number_2, '/', number_3, '/', number_4, '=', result_6)
